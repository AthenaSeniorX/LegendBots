function normalizedRoleName(value) {
    return String(value || '').trim().toLowerCase();
}

function rosterRole(roster, expectedRoleName) {
    const expected = normalizedRoleName(expectedRoleName);
    return roster.find((name) => normalizedRoleName(name) === expected) || null;
}

function membershipMatches(state, roleName, leaderContext) {
    return (
        Boolean(rosterRole(state.teamMembers, roleName)) &&
        Boolean(rosterRole(state.teamMembers, leaderContext.roleName))
    );
}

function reconcileGroupAccountsFromRoster(groupRecord, roster, runId) {
    const reconciledPositions = [];
    for (const accountRecord of groupRecord.accounts) {
        const actualRoleName = rosterRole(
            roster,
            accountRecord.nickname_from_verified_accounts,
        );
        if (!actualRoleName) {
            continue;
        }

        const alreadyConfirmed =
            accountRecord.grouping_status === 'leader_confirmed' ||
            accountRecord.grouping_status === 'membership_confirmed';
        if (!alreadyConfirmed) {
            accountRecord.grouping_status = accountRecord.position === 1
                ? 'leader_confirmed'
                : 'membership_confirmed';
            accountRecord.confirmed_at = nowIso();
            accountRecord.last_error = null;
            accountRecord.evidence = {
                run_id: runId,
                join_mode: 'server_roster_reconciliation',
                source: 'Küçük Takımın sunucu kadrosu',
                roster,
                expected_role_name: accountRecord.nickname_from_verified_accounts,
                actual_role_name: actualRoleName,
                case_insensitive_match: true,
            };
            reconciledPositions.push(accountRecord.position);
        }
        accountRecord.actual_role_name = actualRoleName;
    }

    if (reconciledPositions.length) {
        appendGroupEvent(groupRecord, {
            run_id: runId,
            type: 'accounts_reconciled_from_server_roster',
            positions: reconciledPositions,
            roster,
        });
    }
}

