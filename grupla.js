async function joinLeaderTeam(page, account, leaderContext) {
    const { email } = account;
    console.log(
        `[${email}] Bellekteki lider linki ikinci gizli sekmede açılıyor ` +
        `(iz=${sha256(leaderContext.link).slice(0, 16)}…).`,
    );
    let state = await openEventPage(page, leaderContext.link);
    state = await ensureMemberEventRegistration(
        page,
        state,
        account,
        leaderContext,
    );

    const placeholderNickname = /^(existing[ _-]?account|mevcut[ _-]?hesap)$/i.test(
        String(account.nickname || '').trim(),
    );
    const recoveredRoleName = state.roleName ||
        (state.memberEventRegistration && state.memberEventRegistration.role_name) || '';
    if (state.roleName && !placeholderNickname && state.roleName !== account.nickname) {
        throw new Error(
            `Katılımcı rolü doğrulanmış hesapla eşleşmiyor. ` +
            `Beklenen=${account.nickname}, görünen=${state.roleName}.`,
        );
    }
    if (placeholderNickname && !recoveredRoleName) {
        throw new Error(
            `Yer tutucu hesap kaydı için gerçek rol adı etkinlik sunucusundan çözülemedi.`,
        );
    }
    const roleName = placeholderNickname ? recoveredRoleName : account.nickname;
    const roleNameSource = placeholderNickname
        ? 'event_dom_placeholder_recovery'
        : state.roleName ? 'event_dom' : 'completed_accounts.json';
    if (membershipMatches(state, roleName, leaderContext)) {
        console.log(
            `[${email}] ${roleName} zaten doğru takımda; sunucu listesinden idempotent doğrulandı.`,
        );
        const persistentState = await verifyInvitationClosedTwice(
            page,
            roleName,
            leaderContext,
        );
        leaderContext.knownMembers.add(roleName);
        return {
            roleName,
            state: persistentState,
            alreadyMember: true,
            evidence: {
                join_mode: 'already_in_expected_team',
                role_name_source: roleNameSource,
                event_registration: state.memberEventRegistration,
                api: {
                    request_sent: false,
                    reason: 'Sunucu listesi üyeliği işlem öncesinde doğruladı.',
                },
                server: {
                    expected_leader_role: leaderContext.roleName,
                    expected_member_role: roleName,
                    roster: persistentState.teamMembers,
                    membership_match: true,
                },
                invitation_ui: {
                    dismissed_immediately: true,
                    persistent_closed_checks: 2,
                    persistent_closed: true,
                },
            },
        };
    }

    if (!state.acceptControl.found || state.acceptControl.text !== 'Kabul Et') {
        throw new Error('Görünür davet panelinde "Kabul Et" öğesi bulunamadı.');
    }
    if (!state.acceptControl.parentClass.split(/\s+/).includes('team_invite')) {
        throw new Error(
            `"Kabul Et" öğesi beklenen .team_invite tıklama alanının içinde değil: ` +
            `${state.acceptControl.parentClass || 'sınıf yok'}`,
        );
    }
    if (!state.acceptControl.visible) {
        const verifiedHiddenState = await inspectMembershipWithoutLeavingInvite(
            page,
            roleName,
            leaderContext,
        );
        if (verifiedHiddenState) {
            const persistentState = await verifyInvitationClosedTwice(
                page,
                roleName,
                leaderContext,
            );
            leaderContext.knownMembers.add(roleName);
            return {
                roleName,
                state: persistentState,
                alreadyMember: true,
                evidence: {
                    join_mode: 'hidden_invite_but_expected_team_verified',
                    role_name_source: roleNameSource,
                    event_registration: state.memberEventRegistration,
                    api: {
                        request_sent: false,
                        reason: 'Davet paneli zaten kapalıydı; üyelik sunucu kadrosundan doğrulandı.',
                    },
                    server: {
                        expected_leader_role: leaderContext.roleName,
                        expected_member_role: roleName,
                        roster: persistentState.teamMembers,
                        membership_match: true,
                    },
                    invitation_ui: {
                        dismissed_immediately: false,
                        already_closed_on_load: true,
                        persistent_closed_checks: 2,
                        persistent_closed: true,
                    },
                },
            };
        }

        const error = new Error(
            `"Kabul Et" paneli görünür değil ve beklenen takım doğrulanamadı. ` +
            `Görünen kadro=${state.teamMembers.join(', ') || 'yok'}; ` +
            `uyarılar=${state.visibleAlerts.join(' | ') || 'yok'}.`,
        );
        error.preventRelogin = true;
        throw error;
    }

    await page.waitForFunction(
        () =>
            typeof window.jQuery === 'function' &&
            window.LO &&
            typeof window.LO.agreeTeam === 'function',
        { timeout: CONFIG.actionTimeoutMs },
    );
    console.log(`[${email}] Davet ekranı hazır; ilk tıklamadan önce 3 saniye bekleniyor...`);
    await sleep(3000);

    const requestFields = {
        user_id: requireValue(state.userId, '/agreeTeam için user_id bulunamadı.'),
        um: requireValue(state.um, '/agreeTeam için um bulunamadı.'),
        teamuid: requireValue(state.teamUid, '/agreeTeam için teamud bulunamadı.'),
    };

    let result = null;
    let responseStatus = null;
    let errorCode = null;
    let verifiedState = null;
    let recoveredFromNonSuccessResponse = false;
    let controlledRequestAttempt = 0;
    let cloudFrontRecoveryCount = 0;

    for (let attempt = 1; attempt <= CONFIG.maxAttempts; attempt += 1) {
        controlledRequestAttempt = attempt;
        await waitForNavigationSlot(`[${email}] /agreeTeam isteği`);
        console.log(
            `[${email}] Davet paneli açık tutularak gerçek mouse tıklaması yapılıyor ` +
            `(${attempt}/${CONFIG.maxAttempts})...`,
        );
        result = await clickAcceptWithMouseAndObserve(page, requestFields);
        responseStatus = result.body && result.body.status;
        errorCode = result.body && result.body.err_code;
        const cloudFrontInfo = cloudFrontInfoFromApiResult(result);
        if (cloudFrontInfo.blocked) {
            cloudFrontRecoveryCount += 1;
            if (attempt >= CONFIG.maxAttempts) {
                const retryDelayMs = await waitAfterCloudFrontBlock(
                    cloudFrontRecoveryCount,
                    `[${email}] /agreeTeam isteği`,
                    cloudFrontInfo,
                    { sleep: false },
                );
                const error = new Error(
                    `[${email}] /agreeTeam CloudFront 403 nedeniyle ` +
                    `${CONFIG.maxAttempts} kontrollü denemede tamamlanamadı` +
                    `${cloudFrontInfo.requestId ? ` (Request ID=${cloudFrontInfo.requestId})` : ''}.`,
                );
                error.preventRelogin = true;
                error.cloudFrontBlocked = true;
                error.retryAfterSeconds = Math.ceil(retryDelayMs / 1000);
                throw error;
            }

            await waitAfterCloudFrontBlock(
                cloudFrontRecoveryCount,
                `[${email}] /agreeTeam isteği`,
                cloudFrontInfo,
            );
            state = await openEventPage(page, leaderContext.link);
            if (membershipMatches(state, roleName, leaderContext)) {
                verifiedState = state;
                recoveredFromNonSuccessResponse = true;
                break;
            }
            if (!state.acceptControl.visible) {
                const error = new Error(
                    `[${email}] CloudFront beklemesi sonrasında üyelik doğrulanmadı ve ` +
                    `"Kabul Et" ekranı görünmüyor. Aynı oturum korunarak çalışma durduruldu.`,
                );
                error.preventRelogin = true;
                throw error;
            }
            continue;
        }
        const acceptedResponse =
            (result.ok && responseStatus === 'success') || errorCode === 'ERR312';

        verifiedState = await inspectMembershipWithoutLeavingInvite(
            page,
            roleName,
            leaderContext,
        );
        if (verifiedState) {
            recoveredFromNonSuccessResponse = !acceptedResponse;
            break;
        }

        if (attempt < CONFIG.maxAttempts) {
            await sleep(2000);
            const panelStillVisible = await isAcceptVisible(page);
            console.log(
                `[${email}] 2 saniyelik kontrol: Kabul Et ekranı ` +
                `${panelStillVisible ? 'hâlâ açık' : 'kapalı'}; ` +
                `sunucu üyeliği doğrulanmadığı için aynı oturum korunuyor.`,
            );
            if (!panelStillVisible) {
                await openEventPage(page, leaderContext.link);
            }
        }
    }

    if (!verifiedState) {
        const diagnostics = [
            `sid=${state.sid ? 'var' : 'yok'}`,
            `ud=${state.ud ? 'var' : 'yok'}`,
            `roleid=${state.roleId ? 'var' : 'yok'}`,
            `teamud=${state.teamUid ? 'var' : 'yok'}`,
            `rol_adayı=${(state.roleCandidates || []).length}`,
            `mouse_tıklama=${result ? result.physicalMouseClickCount : 0}`,
            `content-type=${result && result.contentType ? result.contentType : 'yok'}`,
            `status-text=${result && result.httpStatusText ? result.httpStatusText : 'yok'}`,
            `panel_yanıt_süresince_açık=${Boolean(result && result.controlledPanelKeptOpenUntilResponse)}`,
        ].join(', ');
        const error = new Error(
            `/agreeTeam üyeliği aynı oturumda ${CONFIG.maxAttempts} denemede doğrulanamadı: ` +
            `${result ? describeApiFailure(result) : 'yanıt yok'} [${diagnostics}]`,
        );
        error.preventRelogin = true;
        throw error;
    }

    if (!(await closeInvitePanelAfterServerVerification(page))) {
        throw new Error('Sunucu doğrulamasından sonra "Kabul Et" paneli kapatılamadı.');
    }
    await sleep(2000);
    if (await isAcceptVisible(page)) {
        throw new Error('Sunucu doğrulamasından 2 saniye sonra "Kabul Et" ekranı yeniden geldi.');
    }
    console.log(
        `[${email}] Sunucu üyeliği doğrulandı; "Kabul Et" paneli kapandı ve ` +
        `2 saniyelik kontrolde geri gelmedi.`,
    );

    const persistentState = await verifyInvitationClosedTwice(
        page,
        roleName,
        leaderContext,
    );
    leaderContext.knownMembers.add(roleName);
    console.log(
        `[${email}] Üyelik sunucu takım listesinden doğrulandı: ` +
        `${verifiedState.teamMembers.join(', ')}; davet ekranı 2/2 kapalı.`,
    );
    return {
        roleName,
        state: persistentState,
        alreadyMember: errorCode === 'ERR312',
        evidence: {
            join_mode: recoveredFromNonSuccessResponse
                ? 'server_roster_confirmed_after_non_success_response'
                : errorCode === 'ERR312'
                    ? 'server_reported_already_in_team'
                    : 'controlled_physical_mouse_agree_team_success',
            role_name_source: roleNameSource,
            event_registration: state.memberEventRegistration,
            api: {
                endpoint: '/agreeTeam',
                method: 'POST',
                response_observed: true,
                request_payload_fields_verified: ['user_id', 'um', 'teamuid'],
                http_status: result.httpStatus,
                response_status: responseStatus || null,
                error_code: errorCode || null,
                controlled_request_attempts: controlledRequestAttempt,
                cloudfront_recovery_count: cloudFrontRecoveryCount,
                panel_kept_open_until_server_verification: true,
            },
            server: {
                expected_leader_role: leaderContext.roleName,
                expected_member_role: roleName,
                roster: persistentState.teamMembers,
                membership_match: true,
            },
            invitation_ui: {
                dismissed_immediately: false,
                dismissed_only_after_server_verification: true,
                physical_mouse_click_count: result.physicalMouseClickCount,
                devtools_reflow_simulated: true,
                site_navigation_observed: result.navigatedAfterSuccess,
                persistent_closed_checks: 2,
                persistent_closed: true,
            },
        },
    };
}

