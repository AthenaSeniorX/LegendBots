function loadVerifiedGroups(verifiedAccountsPath = VERIFIED_ACCOUNTS_PATH) {
    if (!fs.existsSync(verifiedAccountsPath)) {
        throw new Error(`Doğrulanmış hesap dosyası bulunamadı: ${verifiedAccountsPath}`);
    }

    const sourceText = fs.readFileSync(verifiedAccountsPath, 'utf8');
    const source = parseJson(sourceText);
    if (
        !source ||
        source.version !== 1 ||
        !source.completed_accounts ||
        typeof source.completed_accounts !== 'object' ||
        Array.isArray(source.completed_accounts)
    ) {
        throw new Error('completed_accounts.json biçimi geçersiz veya completed_accounts eksik.');
    }

    const emailPattern = new RegExp(
        `^${escapeRegExp(CONFIG.emailPrefix)}(\\d+)@${escapeRegExp(CONFIG.emailDomain)}$`,
        'i',
    );
    const accounts = [];
    const seenIndexes = new Set();

    for (const [rawEmail, details] of Object.entries(source.completed_accounts)) {
        const email = rawEmail.trim().toLowerCase();
        const match = emailPattern.exec(email);
        if (!match) {
            throw new Error(
                `Doğrulanmış hesap e-posta kalıbıyla eşleşmiyor: ${rawEmail}. ` +
                `Beklenen kalıp: ${CONFIG.emailPrefix}<numara>@${CONFIG.emailDomain}`,
            );
        }
        const index = Number.parseInt(match[1], 10);
        if (!Number.isSafeInteger(index) || index <= 0 || seenIndexes.has(index)) {
            throw new Error(`Geçersiz veya tekrarlanan hesap numarası: ${rawEmail}`);
        }
        if (!details || typeof details !== 'object' || !String(details.nickname || '').trim()) {
            throw new Error(`Doğrulanmış hesapta nickname eksik: ${rawEmail}`);
        }

        seenIndexes.add(index);
        accounts.push({
            index,
            email,
            nickname: String(details.nickname).trim(),
            completedAt: String(details.completed_at || ''),
            sourceVerification: details.verification || {},
        });
    }

    accounts.sort((left, right) => left.index - right.index);
    const groupMap = new Map();
    for (const account of accounts) {
        const groupNumber = Math.floor((account.index - 1) / CONFIG.groupSize) + 1;
        const position = ((account.index - 1) % CONFIG.groupSize) + 1;
        const group = groupMap.get(groupNumber) || {
            groupNumber,
            firstIndex: (groupNumber - 1) * CONFIG.groupSize + 1,
            lastIndex: groupNumber * CONFIG.groupSize,
            accounts: [],
        };
        group.accounts.push({ ...account, groupNumber, position });
        groupMap.set(groupNumber, group);
    }

    const allGroups = Array.from(groupMap.values()).sort(
        (left, right) => left.groupNumber - right.groupNumber,
    );
    const completeGroups = [];
    const incompleteGroups = [];
    for (const group of allGroups) {
        group.accounts.sort((left, right) => left.position - right.position);
        const positions = group.accounts.map((account) => account.position);
        const isComplete =
            group.accounts.length === CONFIG.groupSize &&
            positions.every((position, offset) => position === offset + 1);
        (isComplete ? completeGroups : incompleteGroups).push(group);
    }

    if (!completeGroups.length) {
        throw new Error('Gruplandırılabilecek dört üyeli doğrulanmış hesap grubu yok.');
    }

    return {
        accounts,
        completeGroups,
        incompleteGroups,
        sourceHash: sha256(sourceText),
        sourceVersion: source.version,
    };
}

function createAccountRecord(account, previous = null) {
    const baseStatus = account.position === 1 ? 'leader_pending' : 'membership_pending';
    const previousIsCurrent = Boolean(
        previous &&
        previous.email === account.email &&
        previous.nickname_from_verified_accounts === account.nickname,
    );
    return {
        position: account.position,
        group_role: account.position === 1 ? 'leader' : 'member',
        account_index: account.index,
        email: account.email,
        nickname_from_verified_accounts: account.nickname,
        account_creation_verified_at: account.completedAt || null,
        account_creation_verification: account.sourceVerification,
        source_account_verified: true,
        grouping_status: previousIsCurrent
            ? previous.grouping_status
            : baseStatus,
        actual_role_name: previousIsCurrent
            ? previous.actual_role_name || null
            : null,
        grouping_attempt_count: previousIsCurrent
            ? previous.grouping_attempt_count || 0
            : 0,
        last_attempt_at: previousIsCurrent
            ? previous.last_attempt_at || null
            : null,
        confirmed_at: previousIsCurrent
            ? previous.confirmed_at || null
            : null,
        last_error: previousIsCurrent
            ? previous.last_error || null
            : null,
        evidence: previousIsCurrent
            ? previous.evidence || null
            : null,
        attempt_history: previousIsCurrent
            ? previous.attempt_history || []
            : [],
    };
}

function recomputeGroupRecord(groupRecord) {
    const confirmedPositions = groupRecord.accounts
        .filter((account) =>
            account.grouping_status === 'leader_confirmed' ||
            account.grouping_status === 'membership_confirmed',
        )
        .map((account) => account.position)
        .sort((left, right) => left - right);
    const allConfirmed = confirmedPositions.length === CONFIG.groupSize;

    groupRecord.status = allConfirmed
        ? 'confirmed'
        : confirmedPositions.length
            ? 'partially_confirmed'
            : 'pending';
    groupRecord.verification = {
        expected_member_count: CONFIG.groupSize,
        confirmed_member_count: confirmedPositions.length,
        confirmed_positions: confirmedPositions,
        all_members_confirmed: allConfirmed,
        last_server_roster:
            groupRecord.verification && groupRecord.verification.last_server_roster
                ? groupRecord.verification.last_server_roster
                : [],
        last_checked_at:
            groupRecord.verification && groupRecord.verification.last_checked_at
                ? groupRecord.verification.last_checked_at
                : null,
    };
    groupRecord.confirmed_at = allConfirmed
        ? groupRecord.confirmed_at || nowIso()
        : null;
    groupRecord.updated_at = nowIso();
}

function ensureGroupRecord(state, group) {
    const key = String(group.groupNumber);
    const previous = state.groups[key] || null;
    const previousByPosition = new Map(
        previous && Array.isArray(previous.accounts)
            ? previous.accounts.map((account) => [account.position, account])
            : [],
    );
    const groupRecord = {
        group_number: group.groupNumber,
        account_range: {
            first: group.firstIndex,
            last: group.lastIndex,
        },
        leader_position: 1,
        leader_account_index: group.accounts[0].index,
        status: previous ? previous.status : 'pending',
        confirmed_at: previous ? previous.confirmed_at || null : null,
        created_at: previous ? previous.created_at : nowIso(),
        updated_at: nowIso(),
        invite: previous ? previous.invite || null : null,
        verification: previous
            ? previous.verification || null
            : null,
        event_history: previous && Array.isArray(previous.event_history)
            ? previous.event_history
            : [],
        accounts: group.accounts.map((account) =>
            createAccountRecord(account, previousByPosition.get(account.position)),
        ),
    };
    recomputeGroupRecord(groupRecord);
    state.groups[key] = groupRecord;
    return groupRecord;
}

function loadConfirmedGroupState(sourceData, confirmedGroupsPath = CONFIRMED_GROUPS_PATH) {
    let state = null;
    if (fs.existsSync(confirmedGroupsPath)) {
        const text = fs.readFileSync(confirmedGroupsPath, 'utf8');
        state = parseJson(text);
        if (
            !state ||
            state.version !== 1 ||
            state.file_type !== 'legendbots_confirmed_groups' ||
            !state.groups ||
            typeof state.groups !== 'object'
        ) {
            throw new Error(
                `Mevcut onaylanmış grup dosyası bozuk; güvenlik için duruldu: ` +
                `${confirmedGroupsPath}`,
            );
        }
    }

    if (!state) {
        state = {
            version: 1,
            file_type: 'legendbots_confirmed_groups',
            created_at: nowIso(),
            updated_at: nowIso(),
            grouping_policy: {
                group_size: CONFIG.groupSize,
                leader_position: 1,
                rule: 'Hesap numarasına göre her ardışık dört hesap bir grup; en küçük numara lider.',
                membership_positions: [2, 3, 4],
            },
            source: {},
            runs: [],
            groups: {},
        };
    }
    if (!Array.isArray(state.runs)) {
        state.runs = [];
    }
    for (const previousRun of state.runs) {
        if (previousRun.status === 'running') {
            previousRun.status = 'interrupted';
            previousRun.completed_at = nowIso();
            previousRun.error = {
                at: nowIso(),
                message: 'Önceki süreç kesin sonuç yazmadan kapandı; sonraki çalışmada kurtarıldı.',
            };
        }
    }

    state.source = {
        path: path.basename(VERIFIED_ACCOUNTS_PATH),
        sha256: sourceData.sourceHash,
        schema_version: sourceData.sourceVersion,
        verified_account_count: sourceData.accounts.length,
        complete_group_count: sourceData.completeGroups.length,
        incomplete_groups: sourceData.incompleteGroups.map((group) => ({
            group_number: group.groupNumber,
            expected_account_range: [group.firstIndex, group.lastIndex],
            verified_positions: group.accounts.map((account) => account.position),
            verified_emails: group.accounts.map((account) => account.email),
        })),
        last_loaded_at: nowIso(),
    };
    for (const group of sourceData.completeGroups) {
        ensureGroupRecord(state, group);
    }
    state.updated_at = nowIso();
    return state;
}

function persistConfirmedGroupState(state, confirmedGroupsPath = CONFIRMED_GROUPS_PATH) {
    state.updated_at = nowIso();
    const temporaryPath = `${confirmedGroupsPath}.tmp`;
    const content = `${JSON.stringify(state, null, 2)}\n`;
    let descriptor = null;

    try {
        descriptor = fs.openSync(temporaryPath, 'w');
        fs.writeFileSync(descriptor, content, 'utf8');
        fs.fsyncSync(descriptor);
        fs.closeSync(descriptor);
        descriptor = null;
        if (!fs.existsSync(confirmedGroupsPath)) {
            fs.renameSync(temporaryPath, confirmedGroupsPath);
        } else {
            const backupPath = `${confirmedGroupsPath}.bak`;
            fs.rmSync(backupPath, { force: true });
            fs.renameSync(confirmedGroupsPath, backupPath);
            try {
                fs.renameSync(temporaryPath, confirmedGroupsPath);
                try {
                    fs.rmSync(backupPath, { force: true });
                } catch (_backupCleanupError) {
                    // Yeni ana dosya güvenle yerindedir; eski yedek sonraki yazımda temizlenir.
                }
            } catch (replaceError) {
                if (!fs.existsSync(confirmedGroupsPath) && fs.existsSync(backupPath)) {
                    fs.renameSync(backupPath, confirmedGroupsPath);
                }
                throw replaceError;
            }
        }
    } catch (error) {
        if (descriptor !== null) {
            try {
                fs.closeSync(descriptor);
            } catch (_closeError) {
                // Asıl yazma hatası korunur.
            }
        }
        try {
            fs.rmSync(temporaryPath, { force: true });
        } catch (_cleanupError) {
            // Asıl yazma hatası korunur.
        }
        throw new Error(
            `Onaylanmış grup JSON'u atomik yazılamadı (${confirmedGroupsPath}): ` +
            `${error.message}`,
        );
    }
}

