async function clickAcceptWithMouseAndObserve(page, expectedFields) {
    const requestUrl = new URL('/agreeTeam', page.url()).href;
    let observedResponse = null;
    let responseFailure = null;
    const responsePromise = page.waitForResponse(
        (response) =>
            response.url().split('?')[0] === requestUrl &&
            response.request().method() === 'POST',
        { timeout: CONFIG.actionTimeoutMs },
    ).then((response) => {
        observedResponse = response;
        return response;
    }).catch((error) => {
        responseFailure = error;
        return null;
    });
    await page.evaluate(({ selector, fields }) => {
        const previous = window.__legendControlledJoin;
        if (previous && previous.handler) {
            document.removeEventListener('click', previous.handler, true);
        }

        const state = {
            pending: false,
            result: null,
            handler: null,
        };
        state.handler = async (event) => {
            const clickedAccept = event.target instanceof Element
                ? event.target.closest(selector)
                : null;
            const target = event.target instanceof Element
                ? event.target.closest('.team_invite')
                : null;
            if (!target || !clickedAccept || state.pending || state.result) {
                return;
            }

            event.preventDefault();
            event.stopPropagation();
            event.stopImmediatePropagation();
            state.pending = true;
            try {
                const body = new URLSearchParams();
                Object.entries(fields).forEach(([key, value]) => body.set(key, String(value)));
                const response = await fetch('/agreeTeam', {
                    method: 'POST',
                    credentials: 'same-origin',
                    headers: {
                        'Content-Type': 'application/x-www-form-urlencoded; charset=UTF-8',
                        'X-Requested-With': 'XMLHttpRequest',
                    },
                    body: body.toString(),
                });
                state.result = {
                    status: response.status,
                    statusText: response.statusText,
                    contentType: response.headers.get('content-type'),
                    ok: response.ok,
                    text: await response.text(),
                };
            } catch (error) {
                state.result = {
                    status: 0,
                    statusText: '',
                    contentType: null,
                    ok: false,
                    text: '',
                    fetchError: error && error.message ? error.message : String(error),
                };
            } finally {
                state.pending = false;
            }
        };
        window.__legendControlledJoin = state;
        document.addEventListener('click', state.handler, true);
    }, { selector: ACCEPT_SELECTOR, fields: expectedFields });

    let clickCount = 0;
    for (let attempt = 1; attempt <= 5; attempt += 1) {
        await page.bringToFront();
        await triggerInviteReflow(page);

        const acceptButton = await page.$(ACCEPT_SELECTOR);
        if (!acceptButton) {
            if (observedResponse || !(await isAcceptVisible(page))) {
                break;
            }
            throw new Error('Görünür "Kabul Et" alanı mouse tıklaması için bulunamadı.');
        }
        await acceptButton.scrollIntoView();
        const box = await acceptButton.boundingBox();
        if (!box || box.width <= 0 || box.height <= 0) {
            await sleep(350);
            continue;
        }

        const x = box.x + box.width / 2;
        const y = box.y + box.height / 2;
        await page.mouse.move(x, y, { steps: 8 });
        await page.mouse.down({ button: 'left' });
        await sleep(90);
        await page.mouse.up({ button: 'left' });
        clickCount += 1;

        await Promise.race([responsePromise, sleep(900)]);
        const requestCompleted = await page.evaluate(() => Boolean(
            window.__legendControlledJoin && window.__legendControlledJoin.result
        ));
        if (observedResponse || requestCompleted) {
            break;
        }
    }

    if (!clickCount) {
        throw new Error('"Kabul Et" alanına gerçek mouse olayı gönderilemedi.');
    }

    if (!observedResponse) {
        await responsePromise;
    }

    await page.waitForFunction(
        () => Boolean(window.__legendControlledJoin && window.__legendControlledJoin.result),
        { timeout: CONFIG.actionTimeoutMs },
    );
    const pageResult = await page.evaluate(() => {
        const state = window.__legendControlledJoin;
        if (!state || !state.result) {
            return null;
        }
        document.removeEventListener('click', state.handler, true);
        return state.result;
    });
    if (!pageResult) {
        throw new Error('Kontrollü mouse tıklamasının sayfa içi yanıtı alınamadı.');
    }
    if (!observedResponse) {
        throw new Error(
            `Mouse tıklaması sonrasında /agreeTeam isteği gözlenmedi: ` +
            `${responseFailure ? responseFailure.message : 'zaman aşımı'}`,
        );
    }
    const postedData = new URLSearchParams(observedResponse.request().postData() || '');
    for (const [key, expectedValue] of Object.entries(expectedFields)) {
        if (postedData.get(key) !== String(expectedValue)) {
            throw new Error(
                `/agreeTeam isteğinin ${key} alanı beklenen değerle eşleşmedi.`,
            );
        }
    }

    const observedText = await observedResponse.text().catch(() => '');
    const rawText = pageResult.text || observedText || '';
    const result = {
        httpStatus: pageResult.status || observedResponse.status(),
        httpStatusText: pageResult.statusText || observedResponse.statusText(),
        contentType:
            pageResult.contentType || observedResponse.headers()['content-type'] || null,
        ok: pageResult.ok && observedResponse.ok(),
        body: parseJson(rawText),
        rawText: rawText.trim(),
        invitationDismissedImmediately: false,
        controlledPanelKeptOpenUntilResponse: await isAcceptVisible(page),
        physicalMouseClickCount: clickCount,
        navigatedAfterSuccess: false,
        fetchError: pageResult.fetchError || null,
    };
    return result;
}

async function login(page, email, cloudFrontAttempt = 1) {
    console.log(`[${email}] OAS hesabına giriş yapılıyor...`);
    await gotoWithCloudFrontRecovery(page, CONFIG.loginUrl, {
        waitUntil: 'domcontentloaded',
        timeout: CONFIG.navigationTimeoutMs,
    }, `[${email}] giriş sayfası`);
    await page.waitForSelector('#user_email', { visible: true });
    await page.waitForSelector('#fake_pass_01', { visible: true });
    await page.waitForSelector('.login_btn', { visible: true });

    // Giriş bilgileri DOM'a doğrudan yazılmaz. Gerçek gizli pencere üzerinde
    // alanlara tıklanır ve klavye olaylarıyla, kullanıcı gibi yazılır.
    await page.click('#user_email');
    await page.type('#user_email', email, { delay: 55 });
    await page.click('#fake_pass_01');
    await page.waitForSelector('#user_password', { visible: true });
    await page.click('#user_password');
    await page.type('#user_password', passwordForEmail(email), { delay: 70 });

    await waitForNavigationSlot(`[${email}] giriş gönderimi`, CONFIG.loginUrl);
    const navigationPromise = page
        .waitForNavigation({
            waitUntil: 'networkidle2',
            timeout: CONFIG.navigationTimeoutMs,
        })
        .catch(() => null);
    await page.click('.login_btn');

    const deadline = Date.now() + CONFIG.navigationTimeoutMs;
    let loginCookie = null;
    let detectedCloudFrontInfo = null;
    let nextCloudFrontScanAt = 0;
    while (Date.now() < deadline) {
        const cookies = await page.cookies().catch(() => []);
        loginCookie = cookies.find(
            (cookie) => cookie.name === 'oas_user' && cookie.value,
        );
        if (loginCookie) {
            break;
        }
        if (Date.now() >= nextCloudFrontScanAt) {
            detectedCloudFrontInfo = await cloudFrontPageInfo(page, null);
            if (detectedCloudFrontInfo.blocked) {
                break;
            }
            nextCloudFrontScanAt = Date.now() + 1000;
        }
        await sleep(300);
    }

    if (!loginCookie) {
        const cloudFrontInfo = detectedCloudFrontInfo && detectedCloudFrontInfo.blocked
            ? detectedCloudFrontInfo
            : await cloudFrontPageInfo(page, null);
        if (cloudFrontInfo.blocked && cloudFrontAttempt < CONFIG.cloudFrontMaxAttempts) {
            await waitAfterCloudFrontBlock(
                cloudFrontAttempt,
                `[${email}] giriş yanıtı`,
                cloudFrontInfo,
            );
            return login(page, email, cloudFrontAttempt + 1);
        }
        if (cloudFrontInfo.blocked) {
            const retryDelayMs = await waitAfterCloudFrontBlock(
                cloudFrontAttempt,
                `[${email}] giriş yanıtı`,
                cloudFrontInfo,
                { sleep: false },
            );
            const error = new Error(
                `[${email}] giriş yanıtı CloudFront 403 nedeniyle ` +
                `${CONFIG.cloudFrontMaxAttempts} kontrollü denemede tamamlanamadı` +
                `${cloudFrontInfo.requestId ? ` (Request ID=${cloudFrontInfo.requestId})` : ''}.`,
            );
            error.preventRelogin = true;
            error.cloudFrontBlocked = true;
            error.retryAfterSeconds = Math.ceil(retryDelayMs / 1000);
            throw error;
        }
        const visibleErrors = await page
            .evaluate(() =>
                Array.from(document.querySelectorAll('[class*="error"], [class*="tip"]'))
                    .filter((element) => element.getClientRects().length)
                    .map((element) => String(element.textContent || '').trim())
                    .filter(Boolean)
                    .slice(0, 5),
            )
            .catch(() => []);
        throw new Error(
            `Gerçek giriş düğmesine basıldı ancak oas_user oturumu oluşmadı` +
            `${visibleErrors.length ? `: ${visibleErrors.join(' | ')}` : '.'}`,
        );
    }

    const completedNavigation = await navigationPromise;
    if (!completedNavigation) {
        await page
            .waitForNetworkIdle({ idleTime: 750, timeout: CONFIG.actionTimeoutMs })
            .catch(() => {});
    }
    console.log(`[${email}] Giriş oturumu doğrulandı.`);
}

