function applyRuntimeTiming(timing = {}) {
    const integerAtLeast = (value, fallback, minimum) => {
        const numeric = Number(value);
        return Number.isFinite(numeric) ? Math.max(minimum, Math.round(numeric)) : fallback;
    };
    CONFIG.navigationIntervalMs = integerAtLeast(
        timing.networkIntervalMs,
        CONFIG.navigationIntervalMs,
        3000,
    );
    CONFIG.cloudFrontBackoffMs = integerAtLeast(
        Number(timing.cloudFrontBackoffBaseSeconds) * 1000,
        CONFIG.cloudFrontBackoffMs,
        30000,
    );
    CONFIG.cloudFrontBackoffMaxMs = Math.max(
        CONFIG.cloudFrontBackoffMs,
        integerAtLeast(
            Number(timing.cloudFrontBackoffMaxSeconds) * 1000,
            CONFIG.cloudFrontBackoffMaxMs,
            300000,
        ),
    );
    applySignRuntimeTiming(timing);
    return { ...CONFIG };
}

function sleep(milliseconds) {
    return new Promise((resolve) => setTimeout(resolve, milliseconds));
}

function isCloudFrontSignature(status, text) {
    return status === 403 ||
        /403\s+ERROR/i.test(text || '') ||
        /request could not be satisfied/i.test(text || '') ||
        /generated by cloudfront/i.test(text || '');
}

function retryAfterMilliseconds(raw) {
    if (!raw) {
        return 0;
    }
    const seconds = Number(raw);
    if (Number.isFinite(seconds)) {
        return Math.max(0, seconds * 1000);
    }
    const date = Date.parse(raw);
    return Number.isFinite(date) ? Math.max(0, date - Date.now()) : 0;
}

async function waitForNetworkTurn(label) {
    const cooldownDelay = await networkCooldownDelay('reward');
    if (cooldownDelay > 0) {
        console.log(`[${label}] BOT 4 403 soğuması: ${Math.ceil(cooldownDelay / 1000)} sn.`);
        await sleep(cooldownDelay);
    }
    const gateDelay = await reserveNetworkSlot(ACTIVITY_URL, CONFIG.navigationIntervalMs);
    if (gateDelay > 0) {
        console.log(`[${label}] Ortak CloudFront sırası: ${Math.ceil(gateDelay / 1000)} sn.`);
        await sleep(gateDelay);
    }
}

function parseJsonResponse(text, label) {
    try {
        const parsed = JSON.parse(String(text || ''));
        if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
            throw new Error('nesne değil');
        }
        return parsed;
    } catch (error) {
        throw new Error(`${label} JSON yanıtı çözülemedi: ${error.message}.`);
    }
}

async function postActivityApi(page, endpoint, payload, label) {
    let lastRetryDelayMs = CONFIG.cloudFrontBackoffMs;
    for (let attempt = 1; attempt <= CONFIG.cloudFrontMaxAttempts; attempt += 1) {
        await waitForNetworkTurn(label);
        const result = await page.evaluate(async ({ target, fields, timeoutMs }) => {
            const body = new URLSearchParams(fields).toString();
            const controller = new AbortController();
            const timer = setTimeout(() => controller.abort(), timeoutMs);
            try {
                const response = await fetch(target, {
                    method: 'POST',
                    credentials: 'same-origin',
                    headers: {
                        'Content-Type': 'application/x-www-form-urlencoded; charset=UTF-8',
                        'X-Requested-With': 'XMLHttpRequest',
                    },
                    body,
                    signal: controller.signal,
                });
                return {
                    status: response.status,
                    text: await response.text(),
                    retryAfter: response.headers.get('retry-after') || '',
                };
            } finally {
                clearTimeout(timer);
            }
        }, { target: endpoint, fields: payload, timeoutMs: CONFIG.actionTimeoutMs });

        if (!isCloudFrontSignature(result.status, result.text)) {
            return { ...result, payload: parseJsonResponse(result.text, label) };
        }
        const requestedDelay = computeRateLimitBackoffMs(attempt, {
            baseMs: CONFIG.cloudFrontBackoffMs,
            maximumMs: CONFIG.cloudFrontBackoffMaxMs,
            serverDelayMs: retryAfterMilliseconds(result.retryAfter),
        });
        lastRetryDelayMs = await penalizeNetworkScope(ACTIVITY_URL, requestedDelay, undefined, {
            worker: 'reward',
            attempt,
            http_status: result.status,
        });
        if (attempt < CONFIG.cloudFrontMaxAttempts) {
            console.warn(`[${label}] CloudFront 403; BOT 4 ${Math.ceil(lastRetryDelayMs / 1000)} sn bekleyecek.`);
            await sleep(lastRetryDelayMs);
        }
    }
    const error = new Error(`${label} CloudFront engeli nedeniyle tamamlanamadı.`);
    error.isRateLimited = true;
    error.retryAfterSeconds = Math.ceil(lastRetryDelayMs / 1000);
    throw error;
}

