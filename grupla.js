function findInstalledChrome() {
    const candidates = [
        process.env.LEGEND_CHROME_PATH,
        path.join(process.env.PROGRAMFILES || 'C:\\Program Files', 'Google', 'Chrome', 'Application', 'chrome.exe'),
        path.join(process.env['PROGRAMFILES(X86)'] || 'C:\\Program Files (x86)', 'Google', 'Chrome', 'Application', 'chrome.exe'),
        process.env.LOCALAPPDATA
            ? path.join(process.env.LOCALAPPDATA, 'Google', 'Chrome', 'Application', 'chrome.exe')
            : null,
    ].filter(Boolean);
    return candidates.find((candidate) => fs.existsSync(candidate)) || null;
}

async function launchAutomationBrowser() {
    const executablePath = findInstalledChrome();
    if (!executablePath) {
        throw new Error(
            'Kurulu Google Chrome bulunamadı. LEGEND_CHROME_PATH ile chrome.exe yolu verilmelidir.',
        );
    }
    console.log(`Kurulu Google Chrome kullanılıyor: ${executablePath}`);
    return puppeteer.launch({
        executablePath,
        headless: CONFIG.headless,
        defaultViewport: { width: 1366, height: 900 },
        args: CONFIG.headless
            ? ['--no-first-run', '--no-default-browser-check']
            : [
                '--incognito',
                '--start-minimized',
                '--window-position=-32000,-32000',
                '--window-size=1366,900',
                '--no-first-run',
                '--no-default-browser-check',
            ],
    });
}

async function processAccount(account, leaderContext, browser) {
    const { email, index, position } = account;
    const incognitoContext = await browser.createBrowserContext();

    try {
        const loginPage = await incognitoContext.newPage();
        configureAutomationPage(loginPage);
        await login(loginPage, email);

        if (position === 1) {
            if (!account.positionAssignedByQueue && !isLeader(index)) {
                throw new Error(`${index}. hesap 1. üye olduğu halde lider numarası kuralına uymuyor.`);
            }
            return {
                kind: 'leader',
                leaderContext: await createLeaderContext(loginPage, account),
            };
        }

        if (!leaderContext) {
            throw new Error(
                `${index}. hesap takım lideri değil ancak aktif lider davet linki yok. ` +
                `Çalışma aralığı i % ${CONFIG.groupSize} === 1 olan bir liderle başlamalıdır.`,
            );
        }

        console.log(
            `[${email}] Giriş ilk gizli sekmede doğrulandı; lider linki için ikinci sekme açılıyor...`,
        );
        const invitationPage = await incognitoContext.newPage();
        configureAutomationPage(invitationPage);
        await invitationPage.bringToFront();
        const membership = await joinLeaderTeam(
            invitationPage,
            account,
            leaderContext,
        );
        membership.evidence.browser_flow = {
            private_browser_context: true,
            login_tab: 'first_tab',
            invitation_tab: 'second_tab',
            leader_link_source: 'in_memory_from_group_leader',
            same_private_context_for_member_tabs: true,
        };
        return { kind: 'member', membership };
    } finally {
        await incognitoContext.close().catch(() => {});
        console.log(`[${email}] Gizli hesap oturumu kapatıldı.`);
    }
}

function accountRecordFor(groupRecord, position) {
    const record = groupRecord.accounts.find((account) => account.position === position);
    if (!record) {
        throw new Error(
            `${groupRecord.group_number}. grup içinde ${position}. üye kaydı bulunamadı.`,
        );
    }
    return record;
}

function pushAttemptHistory(accountRecord, entry) {
    accountRecord.attempt_history.push({ at: nowIso(), ...entry });
    if (accountRecord.attempt_history.length > 50) {
        accountRecord.attempt_history = accountRecord.attempt_history.slice(-50);
    }
}

function recordLeaderSuccess(state, groupRecord, accountRecord, result, runId) {
    const context = result.leaderContext;
    const confirmedAt = nowIso();
    accountRecord.grouping_status = 'leader_confirmed';
    accountRecord.actual_role_name = context.roleName;
    accountRecord.confirmed_at = confirmedAt;
    accountRecord.last_error = null;
    accountRecord.evidence = {
        run_id: runId,
        role_matches_verified_nickname:
            context.roleName === accountRecord.nickname_from_verified_accounts,
        role_name_source: context.roleNameSource,
        role_binding_verified: true,
        server_id: context.serverId,
        role_id: context.roleId,
        initial_server_roster: context.initialRoster,
        invite_link: {
            full_url_stored: false,
            reason: 'um ve kullanıcı kimliği içeren davet URLsi güvenlik için saklanmaz.',
            sha256: sha256(context.link),
            runtime_lifetime: 'memory_only_until_current_group_finishes',
        },
    };
    pushAttemptHistory(accountRecord, {
        run_id: runId,
        attempt: accountRecord.grouping_attempt_count,
        outcome: 'leader_confirmed',
        role_name: context.roleName,
    });

    groupRecord.invite = {
        generated_at: confirmedAt,
        generated_by_position: 1,
        generated_by_account_index: accountRecord.account_index,
        generated_by_email: accountRecord.email,
        invitation_type: 'team',
        endpoint_origin: new URL(CONFIG.eventUrl).origin,
        query_fields: ['sid', 'ud', 'type', 'shareuid', 'um'],
        full_url_stored: false,
        url_sha256: sha256(context.link),
        runtime_lifetime: 'memory_only_until_current_group_finishes',
        server_id: context.serverId,
    };
    groupRecord.verification.last_server_roster = context.initialRoster;
    groupRecord.verification.last_checked_at = confirmedAt;
    appendGroupEvent(groupRecord, {
        run_id: runId,
        type: 'leader_invite_generated',
        account_index: accountRecord.account_index,
        role_name: context.roleName,
        invite_url_sha256: sha256(context.link),
    });
    reconcileGroupAccountsFromRoster(
        groupRecord,
        context.initialRoster,
        runId,
    );
    recomputeGroupRecord(groupRecord);
    persistConfirmedGroupState(state);
}

