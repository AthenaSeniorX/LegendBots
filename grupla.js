async function postFormAndObserve(page, endpoint, fields) {
    const requestUrl = new URL(endpoint, page.url()).href;
    const responsePromise = page.waitForResponse(
        (response) =>
            response.url().split('?')[0] === requestUrl &&
            response.request().method() === 'POST',
        { timeout: CONFIG.actionTimeoutMs },
    );

    const browserFetchPromise = page.evaluate(async ({ path, formFields }) => {
        const body = new URLSearchParams();
        Object.entries(formFields).forEach(([key, value]) => body.set(key, String(value)));

        const response = await fetch(path, {
            method: 'POST',
            credentials: 'same-origin',
            headers: {
                'Content-Type': 'application/x-www-form-urlencoded; charset=UTF-8',
                'X-Requested-With': 'XMLHttpRequest',
            },
            body: body.toString(),
        });

        return {
            status: response.status,
            text: await response.text(),
        };
    }, { path: endpoint, formFields: fields });

    const [browserResult, observedResponse] = await Promise.all([
        browserFetchPromise,
        responsePromise,
    ]);
    const observedText = await observedResponse.text().catch(() => '');
    const rawText = observedText || browserResult.text || '';

    return {
        httpStatus: observedResponse.status(),
        ok: observedResponse.ok(),
        body: parseJson(rawText),
        rawText: rawText.trim(),
    };
}

async function postFormWithCloudFrontRecovery(page, endpoint, fields, label) {
    let lastResult = null;
    let lastInfo = null;
    let lastRetryDelayMs = CONFIG.cloudFrontBackoffMs;
    for (let attempt = 1; attempt <= CONFIG.cloudFrontMaxAttempts; attempt += 1) {
        await waitForNavigationSlot(label);
        lastResult = await postFormAndObserve(page, endpoint, fields);
        lastInfo = cloudFrontInfoFromApiResult(lastResult);
        if (!lastInfo.blocked) {
            return lastResult;
        }
        lastRetryDelayMs = await waitAfterCloudFrontBlock(attempt, label, lastInfo, {
            sleep: attempt < CONFIG.cloudFrontMaxAttempts,
        });
    }

    const error = new Error(
        `${label}: CloudFront 403, ${CONFIG.cloudFrontMaxAttempts} kontrollü API denemesinde açılmadı` +
        `${lastInfo && lastInfo.requestId ? ` (Request ID=${lastInfo.requestId})` : ''}.`,
    );
    error.preventRelogin = true;
    error.cloudFrontBlocked = true;
    error.retryAfterSeconds = Math.ceil(lastRetryDelayMs / 1000);
    throw error;
}

