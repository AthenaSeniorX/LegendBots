async function promptStartSelection(sourceData) {
    const groupNumbers = sourceData.completeGroups.map((group) => group.groupNumber);
    console.log('\nDoğrulanmış ve gruplandırmaya hazır hesaplar:');
    for (const group of sourceData.completeGroups) {
        console.log(
            `  ${group.groupNumber}. grup: ` +
            group.accounts.map((account) => `${account.position}. üye=${account.email}`).join(' | '),
        );
    }
    if (sourceData.incompleteGroups.length) {
        console.log('\nEksik olduğu için işlenmeyecek gruplar:');
        for (const group of sourceData.incompleteGroups) {
            console.log(
                `  ${group.groupNumber}. grup (${group.firstIndex}-${group.lastIndex}): ` +
                `doğrulanmış konumlar ${group.accounts.map((account) => account.position).join(', ')}`,
            );
        }
    }

    const terminal = readline.createInterface({ input: process.stdin, output: process.stdout });
    const answers = terminal[Symbol.asyncIterator]();
    const ask = async (question) => {
        process.stdout.write(question);
        const answer = await answers.next();
        if (answer.done) {
            throw new Error('Başlangıç seçimi tamamlanmadan giriş akışı kapandı.');
        }
        return answer.value;
    };
    try {
        let groupNumber = null;
        while (!groupNumbers.includes(groupNumber)) {
            const answer = await ask(
                `\nHangi gruptan başlansın? (${groupNumbers.join(', ')}): `,
            );
            groupNumber = Number.parseInt(answer.trim(), 10);
            if (!groupNumbers.includes(groupNumber)) {
                console.log('Lütfen listelenen hazır grup numaralarından birini girin.');
            }
        }

        let memberPosition = null;
        while (![1, 2, 3, 4].includes(memberPosition)) {
            const answer = await ask(
                'O grubun kaçıncı üyesinden başlansın? (1=lider, 2/3/4=üye): ',
            );
            memberPosition = Number.parseInt(answer.trim(), 10);
            if (![1, 2, 3, 4].includes(memberPosition)) {
                console.log('Üye konumu 1, 2, 3 veya 4 olmalıdır.');
            }
        }

        return { groupNumber, memberPosition };
    } finally {
        terminal.close();
    }
}

function buildExecutionPlan(sourceData, selection) {
    const startOffset = sourceData.completeGroups.findIndex(
        (group) => group.groupNumber === selection.groupNumber,
    );
    if (startOffset < 0) {
        throw new Error(`Seçilen ${selection.groupNumber}. grup hazır gruplar arasında yok.`);
    }

    return sourceData.completeGroups.slice(startOffset).map((group, offset) => ({
        group,
        requestedStartPosition: offset === 0 ? selection.memberPosition : 1,
        memberStartPosition: offset === 0
            ? Math.max(2, selection.memberPosition)
            : 2,
    }));
}

